def _run_codex_analysis(plan_path: Path, target_root: Path, model: str) -> Dict[str, Any]:
    plan_text = read_text(plan_path)
    prompt = _build_analysis_prompt(plan_path, plan_text)
    return _run_codex_json(
        prompt=prompt,
        schema=_analysis_schema(),
        target_root=target_root,
        model=model,
        tag="analysis",
    )


def _normalize_analysis(raw_analysis: Dict[str, Any], plan_path: Path) -> Dict[str, Any]:
    generated_at = iso_now()

    summary = str(raw_analysis.get("summary", "")).strip()
    if len(summary) < 20:
        raise SystemExit("codex summary is too short; expected detailed generation summary")

    feature_list = _normalize_feature_list(raw_analysis.get("feature_list"))
    task_plan = _normalize_task_plan(raw_analysis.get("task_plan"), plan_path, generated_at)

    required_map: Dict[str, str] = {}
    required_order: List[str] = []

    raw_required = raw_analysis.get("required_files")
    if not isinstance(raw_required, list) or not raw_required:
        raise SystemExit("required_files must be a non-empty list")

    for idx, entry in enumerate(raw_required, start=1):
        if not isinstance(entry, dict):
            raise SystemExit(f"required_files[{idx}] must be an object")
        raw_path = str(entry.get("path", ""))
        path = normalize_rel_path(raw_path)
        reason = str(entry.get("reason", "")).strip() or DEFAULT_REASON
        if path not in required_map:
            required_order.append(path)
        required_map[path] = reason

    missing_core_required = [path for path in CORE_REQUIRED_PATHS if path not in required_map]
    if missing_core_required:
        raise SystemExit(
            "required_files missing core paths: " + ", ".join(missing_core_required)
        )

    required_files = [
        {"path": path, "reason": required_map[path]} for path in required_order
    ]

    return {
        "generated_at": generated_at,
        "plan_path": str(plan_path.resolve()),
        "source": "codex",
        "summary": summary,
        "required_files": required_files,
        "feature_list": feature_list,
        "task_plan": task_plan,
    }


def _run_codex_single_file(
    *,
    plan_path: Path,
    target_root: Path,
    model: str,
    analysis: Dict[str, Any],
    target_path: str,
    reason: str,
    index: int,
    total: int,
) -> Dict[str, Any]:
    plan_text = read_text(plan_path)
    prompt = _build_file_prompt(
        plan_path=plan_path,
        plan_text=plan_text,
        summary=analysis["summary"],
        required_files=analysis["required_files"],
        feature_list=analysis["feature_list"],
        task_plan=analysis["task_plan"],
        target_path=target_path,
        reason=reason,
        index=index,
        total=total,
    )
    tag = f"file_{index:03d}_{target_path}"
    result = _run_codex_json(
        prompt=prompt,
        schema=_single_file_schema(target_path),
        target_root=target_root,
        model=model,
        tag=tag,
    )

    path = normalize_rel_path(str(result.get("path", "")))
    if path != target_path:
        raise SystemExit(f"single-file codex output path mismatch: expected {target_path}, got {path}")

    content = result.get("content")
    if not isinstance(content, str):
        raise SystemExit(f"single-file codex output content must be string: {target_path}")

    gen_reason = str(result.get("reason", "")).strip() or reason or DEFAULT_REASON
    executable = bool(result.get("executable", False))

    return {
        "path": target_path,
        "content": _ensure_text_with_newline(content),
        "reason": gen_reason,
        "executable": executable,
    }


def _run_codex_file_bundle(
    *,
    plan_path: Path,
    target_root: Path,
    model: str,
) -> Dict[str, Any]:
    plan_text = read_text(plan_path)
    prompt = _build_file_bundle_prompt(
        plan_path=plan_path,
        plan_text=plan_text,
    )
    raw_bundle = _run_codex_json(
        prompt=prompt,
        schema=_file_bundle_schema(),
        target_root=target_root,
        model=model,
        tag="bundle_all",
    )

    analysis = _normalize_analysis(raw_bundle, plan_path=plan_path)
    required_files: List[Dict[str, str]] = analysis["required_files"]
    required_paths = [item["path"] for item in required_files]
    required_paths_set = set(required_paths)
    required_map = {item["path"]: item["reason"] for item in required_files}

    raw_files = raw_bundle.get("files")
    if not isinstance(raw_files, list) or not raw_files:
        raise SystemExit("bundle files must be a non-empty list")

    file_map: Dict[str, str] = {}
    file_order: List[str] = []
    executable_paths: Set[str] = set()

    for idx, entry in enumerate(raw_files, start=1):
        if not isinstance(entry, dict):
            raise SystemExit(f"bundle files[{idx}] must be an object")

        path = normalize_rel_path(str(entry.get("path", "")))
        if path not in required_paths_set:
            raise SystemExit(f"bundle files[{idx}].path is not declared in required_files: {path}")
        if path in file_map:
            raise SystemExit(f"bundle files has duplicate path: {path}")

        content = entry.get("content")
        if not isinstance(content, str):
            raise SystemExit(f"bundle files[{idx}].content must be a string: {path}")

        reason = str(entry.get("reason", "")).strip() or required_map[path] or DEFAULT_REASON
        executable = bool(entry.get("executable", False))

        required_map[path] = reason
        file_map[path] = _ensure_text_with_newline(content)
        file_order.append(path)
        if executable or path.endswith((".sh", ".sbatch")):
            executable_paths.add(path)

    if file_order != required_paths:
        raise SystemExit(
            "bundle files order mismatch with required_files order; "
            "expected exact same ordered paths"
        )

    normalized_required_files = [{"path": path, "reason": required_map[path]} for path in required_paths]
    return {
        "generated_at": analysis["generated_at"],
        "plan_path": analysis["plan_path"],
        "source": analysis["source"],
        "summary": analysis["summary"],
        "feature_list": analysis["feature_list"],
        "task_plan": analysis["task_plan"],
        "required_files": normalized_required_files,
        "file_map": file_map,
        "file_order": file_order,
        "executable_paths": sorted(executable_paths),
    }


def _materialize_generated_bundle(
    *,
    plan_path: Path,
    target_root: Path,
    model: str,
) -> Dict[str, Any]:
    file_bundle = _run_codex_file_bundle(
        plan_path=plan_path,
        target_root=target_root,
        model=model,
    )

    required_files = file_bundle["required_files"]
    required_paths = [item["path"] for item in required_files]
    required_map = {item["path"]: item["reason"] for item in required_files}

    file_map: Dict[str, str] = dict(file_bundle["file_map"])
    file_order: List[str] = list(file_bundle["file_order"])
    executable_paths: Set[str] = set(file_bundle["executable_paths"])

    # Keep gate files deterministic and validated locally.
    file_map[".codex-research/feature_list.json"] = as_json_pretty(file_bundle["feature_list"])
    file_map[".codex-research/task_plan.json"] = as_json_pretty(file_bundle["task_plan"])
    file_map[CN_EXEC_GUIDE_PATH] = _build_cn_execution_guide(
        plan_path=plan_path,
        target_root=target_root,
    )
    if WORKFLOW_CODEX_PATH in file_map:
        file_map[WORKFLOW_CODEX_PATH] = _ensure_workflow_github_channel(
            file_map[WORKFLOW_CODEX_PATH]
        )

    missing_required = [path for path in required_paths if path not in file_map]
    if missing_required:
        raise SystemExit("generated files missing required paths: " + ", ".join(missing_required))

    required_payload = {
        "generated_at": file_bundle["generated_at"],
        "plan_path": file_bundle["plan_path"],
        "source": "codex",
        "summary": file_bundle["summary"],
        "required_files": required_files,
    }
    file_map[".codex-research/required_files.json"] = as_json_pretty(required_payload)

    missing_core = [
        path for path in CORE_REQUIRED_PATHS if path not in required_map or path not in file_map
    ]
    if missing_core:
        raise SystemExit("generated files missing core paths: " + ", ".join(missing_core))

    return {
        "generated_at": file_bundle["generated_at"],
        "plan_path": file_bundle["plan_path"],
        "source": file_bundle["source"],
        "summary": file_bundle["summary"],
        "required_files": required_files,
        "feature_list": file_bundle["feature_list"],
        "task_plan": file_bundle["task_plan"],
        "file_map": file_map,
        "file_order": file_order,
        "executable_paths": sorted(executable_paths),
    }


def load_bundle(plan_path: Path, target_root: Path, codex_model: str) -> Dict[str, Any]:
    return _materialize_generated_bundle(
        plan_path=plan_path,
        target_root=target_root,
        model=codex_model,
    )


def load_analysis(plan_path: Path, target_root: Path, codex_model: str) -> Dict[str, Any]:
    raw_analysis = _run_codex_analysis(plan_path=plan_path, target_root=target_root, model=codex_model)
    return _normalize_analysis(raw_analysis, plan_path=plan_path)


def write_file(path: Path, content: str, force: bool) -> Tuple[bool, str]:
    if path.exists() and not force:
        return False, "exists"
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    return True, "written"


def chmod_if_shell(path: Path) -> None:
    if path.suffix in {".sh", ".sbatch"}:
        current = path.stat().st_mode
        path.chmod(current | 0o111)


def cmd_extract(args: argparse.Namespace) -> int:
    plan_path = Path(args.plan).resolve()
    target_root = Path(args.target).resolve() if args.target else plan_path.parent
    out_path = Path(args.out).resolve()

    analysis = load_analysis(plan_path=plan_path, target_root=target_root, codex_model=args.codex_model)

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(
        as_json_pretty(
            {
                "generated_at": analysis["generated_at"],
                "plan_path": analysis["plan_path"],
                "source": analysis["source"],
                "summary": analysis["summary"],
                "required_files": analysis["required_files"],
                "feature_list": analysis["feature_list"],
                "task_plan": analysis["task_plan"],
            }
        ),
        encoding="utf-8",
    )
    print(f"[extract] wrote {out_path}")
    return 0


def bootstrap_from_bundle(
    target_root: Path,
    bundle: Dict[str, Any],
    force: bool,
) -> Tuple[int, int]:
    file_map: Dict[str, str] = bundle["file_map"]
    file_order: List[str] = bundle.get("file_order", [])
    executable_paths: Set[str] = set(bundle["executable_paths"])

    created = 0
    skipped = 0

    write_paths = file_order if file_order else sorted(file_map.keys())
    for rel_path in write_paths:
        abs_path = target_root / rel_path
        content = file_map[rel_path]
        wrote, status = write_file(abs_path, content, force)
        if wrote:
            if rel_path in executable_paths or abs_path.suffix in {".sh", ".sbatch"}:
                chmod_if_shell(abs_path)
            created += 1
            print(f"[bootstrap] created {abs_path}")
        else:
            skipped += 1
            print(f"[bootstrap] skipped {abs_path} ({status})")

    return created, skipped


def cmd_bootstrap(args: argparse.Namespace) -> int:
    plan_path = Path(args.plan).resolve()
    target_root = Path(args.target).resolve()
    bundle = load_bundle(plan_path=plan_path, target_root=target_root, codex_model=args.codex_model)

    created, skipped = bootstrap_from_bundle(target_root, bundle, args.force)
    print(f"[bootstrap] done: created={created}, skipped={skipped}")
    return 0


def cmd_gen_doc(args: argparse.Namespace) -> int:
    plan_path = Path(args.plan).resolve()
    target_root = Path(args.target).resolve()
    analysis = load_analysis(plan_path=plan_path, target_root=target_root, codex_model=args.codex_model)

    # Only one file generation call for gen-doc mode.
    mechanism_entry = _run_codex_single_file(
        plan_path=plan_path,
        target_root=target_root,
        model=args.codex_model,
        analysis=analysis,
        target_path=".codex-research/MECHANISM.md",
        reason="In-project mechanism introduction document.",
        index=1,
        total=1,
    )

    mechanism_path = target_root / mechanism_entry["path"]
    mechanism_path.parent.mkdir(parents=True, exist_ok=True)
    mechanism_path.write_text(mechanism_entry["content"], encoding="utf-8")
    print(f"[doc] wrote {mechanism_path}")
    return 0


def cmd_all(args: argparse.Namespace) -> int:
    plan_path = Path(args.plan).resolve()
    target_root = Path(args.target).resolve()
    bundle = load_bundle(plan_path=plan_path, target_root=target_root, codex_model=args.codex_model)

    extract_path = target_root / ".codex-research/required_files.generated.json"
    extract_path.parent.mkdir(parents=True, exist_ok=True)
    extract_path.write_text(
        as_json_pretty(
            {
                "generated_at": bundle["generated_at"],
                "plan_path": bundle["plan_path"],
                "source": bundle["source"],
                "summary": bundle["summary"],
                "required_files": bundle["required_files"],
                "feature_list": bundle["feature_list"],
                "task_plan": bundle["task_plan"],
            }
        ),
        encoding="utf-8",
    )

    created, skipped = bootstrap_from_bundle(target_root, bundle, args.force)

    mechanism_path = target_root / ".codex-research/MECHANISM.md"
    print(f"[all] wrote manifest: {extract_path}")
    print(f"[all] wrote mechanism: {mechanism_path}")
    print(f"[all] done: created={created}, skipped={skipped}")
    return 0


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Codex-driven long-running harness bootstrapper for DL research.",
    )
    sub = parser.add_subparsers(dest="cmd", required=True)

    extract = sub.add_parser("extract", help="Generate Codex-derived manifest from a plan")
    extract.add_argument("--plan", required=True, help="Path to uploaded plan file")
    extract.add_argument("--out", required=True, help="Output JSON path")
    extract.add_argument(
        "--target",
        required=False,
        help="Optional target project root for Codex execution context",
    )
    extract.add_argument("--codex-model", default="", help="Optional Codex model override")
    extract.set_defaults(func=cmd_extract)

    bootstrap = sub.add_parser("bootstrap", help="Create harness files in target project")
    bootstrap.add_argument("--plan", required=True, help="Path to uploaded plan file")
    bootstrap.add_argument("--target", required=True, help="Target project root")
    bootstrap.add_argument("--force", action="store_true", help="Overwrite existing files")
    bootstrap.add_argument("--codex-model", default="", help="Optional Codex model override")
    bootstrap.set_defaults(func=cmd_bootstrap)

    gen_doc = sub.add_parser("gen-doc", help="Generate mechanism markdown in project")
    gen_doc.add_argument("--plan", required=True, help="Path to uploaded plan file")
    gen_doc.add_argument("--target", required=True, help="Target project root")
    gen_doc.add_argument("--codex-model", default="", help="Optional Codex model override")
    gen_doc.set_defaults(func=cmd_gen_doc)

    all_cmd = sub.add_parser("all", help="Extract + bootstrap + generate doc")
    all_cmd.add_argument("--plan", required=True, help="Path to uploaded plan file")
    all_cmd.add_argument("--target", required=True, help="Target project root")
    all_cmd.add_argument("--force", action="store_true", help="Overwrite existing files")
    all_cmd.add_argument("--codex-model", default="", help="Optional Codex model override")
    all_cmd.set_defaults(func=cmd_all)

    return parser


def main() -> int:
    parser = build_parser()
    args = parser.parse_args()

    plan_path = Path(getattr(args, "plan", ""))
    if plan_path and not plan_path.exists():
        raise SystemExit(f"Plan file not found: {plan_path}")

    return args.func(args)


if __name__ == "__main__":
    raise SystemExit(main())
