def iso_now() -> str:
    return dt.datetime.now(dt.timezone.utc).replace(microsecond=0).isoformat()


def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def as_json_pretty(data: object) -> str:
    return json.dumps(data, ensure_ascii=False, indent=2) + "\n"


def ensure_codex_cli() -> None:
    if shutil.which("codex") is None:
        raise SystemExit("codex CLI is not available in PATH")


def normalize_rel_path(path: str) -> str:
    normalized = path.strip().replace("\\", "/")
    if not normalized.startswith(".codex-research/"):
        raise SystemExit(f"generated path must be under .codex-research/: {path}")
    parts = Path(normalized).parts
    if ".." in parts:
        raise SystemExit(f"generated path must not contain '..': {path}")
    return normalized


