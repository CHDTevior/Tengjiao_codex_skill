def _single_file_schema(target_path: str) -> Dict[str, Any]:
    return {
        "type": "object",
        "additionalProperties": False,
        "required": ["path", "content", "reason", "executable"],
        "properties": {
            "path": {"type": "string", "const": target_path},
            "content": {"type": "string"},
            "reason": {"type": "string", "minLength": 3},
            "executable": {"type": "boolean"},
        },
    }


def _file_bundle_schema() -> Dict[str, Any]:
    analysis_schema = _analysis_schema()
    analysis_properties = dict(analysis_schema["properties"])
    analysis_properties["files"] = {
        "type": "array",
        "minItems": 1,
        "items": {
            "type": "object",
            "additionalProperties": False,
            "required": ["path", "content", "reason", "executable"],
            "properties": {
                "path": {"type": "string", "minLength": 5},
                "content": {"type": "string"},
                "reason": {"type": "string", "minLength": 3},
                "executable": {"type": "boolean"},
            },
        },
    }
    return {
        "type": "object",
        "additionalProperties": False,
        "required": ["summary", "required_files", "feature_list", "task_plan", "files"],
        "properties": analysis_properties,
    }


def _log_paths(target_root: Path, tag: str) -> Tuple[Path, Path, Path]:
    log_root = target_root / ".codex-research" / "plan" / "codex_generation_logs"
    log_root.mkdir(parents=True, exist_ok=True)

    safe_tag = re.sub(r"[^a-zA-Z0-9_.-]+", "_", tag)[:120] or "codex"
    stamp = dt.datetime.now(dt.timezone.utc).strftime("%Y%m%d_%H%M%S_%f")

    schema_path = log_root / f"{stamp}_{safe_tag}.schema.json"
    output_path = log_root / f"{stamp}_{safe_tag}.output.json"
    log_path = log_root / f"{stamp}_{safe_tag}.exec.log"
    return schema_path, output_path, log_path


def _run_codex_json(
    *,
    prompt: str,
    schema: Dict[str, Any],
    target_root: Path,
    model: str,
    tag: str,
) -> Dict[str, Any]:
    ensure_codex_cli()
    schema_path, output_path, log_path = _log_paths(target_root, tag)
    schema_path.write_text(as_json_pretty(schema), encoding="utf-8")

    cmd: List[str] = [
        "codex",
        "exec",
        "--skip-git-repo-check",
        "--ephemeral",
        "-C",
        str(target_root),
        "--output-schema",
        str(schema_path),
        "--output-last-message",
        str(output_path),
        "-",
    ]
    if model:
        cmd.extend(["--model", model])

    proc = subprocess.run(
        cmd,
        input=prompt,
        text=True,
        capture_output=True,
        check=False,
    )

    log_path.write_text(
        "CMD\n"
        + " ".join(cmd)
        + "\n\nSTDOUT\n"
        + proc.stdout
        + "\n\nSTDERR\n"
        + proc.stderr
        + "\n",
        encoding="utf-8",
    )

    if proc.returncode != 0:
        raise SystemExit(
            "codex generation failed "
            f"(exit={proc.returncode}, tag={tag}). See log: {log_path}"
        )

    if not output_path.exists():
        raise SystemExit(
            f"codex generation produced no output JSON (tag={tag}). Log: {log_path}"
        )

    try:
        return json.loads(output_path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        raise SystemExit(
            f"failed to parse codex JSON output (tag={tag}): {output_path}: {exc}"
        ) from exc


