#!/usr/bin/env python3
"""Codex-driven long-running harness generator for DL research projects.

Generation strategy:
1. For bootstrap/all mode, run one Codex bundle call that returns planning
   metadata (summary, required_files, feature_list, task_plan) plus ordered
   file contents.
2. Validate outputs and materialize files under .codex-research/.
3. Keep extract/gen-doc mode available with narrower Codex calls.
"""

