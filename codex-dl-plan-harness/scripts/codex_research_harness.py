#!/usr/bin/env python3
"""Codex-driven long-running harness generator for DL research projects.

This script no longer performs keyword/task-line rule extraction for scaffold
material. Instead, it delegates scaffold design and file content generation to
Codex, then validates and writes the resulting artifact bundle.
"""

from __future__ import annotations

import argparse
import datetime as dt
import json
import shutil
import subprocess
import tempfile
from pathlib import Path
from typing import Any, Dict, List, Set, Tuple


CORE_REQUIRED_PATHS = [
    ".codex-research/research_spec.md",
    ".codex-research/feature_list.json",
    ".codex-research/task_plan.json",
    ".codex-research/required_files.json",
    ".codex-research/session_progress.md",
    ".codex-research/decision_log.md",
    ".codex-research/run_registry.jsonl",
    ".codex-research/init.sh",
    ".codex-research/checks/smoke_test.sh",
    ".codex-research/prompts/initializer.md",
    ".codex-research/prompts/worker.md",
    ".codex-research/workflow/CODEX.md",
    ".codex-research/run_one_task.sh",
    ".codex-research/run_plan.sh",
    ".codex-research/MECHANISM.md",
]

DEFAULT_REASON = "Generated by Codex from the uploaded research plan."


def iso_now() -> str:
    return dt.datetime.now(dt.timezone.utc).replace(microsecond=0).isoformat()


def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def as_json_pretty(data: object) -> str:
    return json.dumps(data, ensure_ascii=False, indent=2) + "\n"


def ensure_codex_cli() -> None:
    if shutil.which("codex") is None:
        raise SystemExit("codex CLI is not available in PATH")


def normalize_rel_path(path: str) -> str:
    normalized = path.strip().replace("\\", "/")
    if not normalized.startswith(".codex-research/"):
        raise SystemExit(f"generated path must be under .codex-research/: {path}")
    parts = Path(normalized).parts
    if ".." in parts:
        raise SystemExit(f"generated path must not contain '..': {path}")
    return normalized


def _json_schema() -> Dict[str, object]:
    return {
        "type": "object",
        "additionalProperties": False,
        "required": ["summary", "required_files", "feature_list", "task_plan", "files"],
        "properties": {
            "summary": {"type": "string", "minLength": 20},
            "required_files": {
                "type": "array",
                "minItems": 1,
                "items": {
                    "type": "object",
                    "additionalProperties": False,
                    "required": ["path", "reason"],
                    "properties": {
                        "path": {"type": "string", "minLength": 5},
                        "reason": {"type": "string", "minLength": 3},
                    },
                },
            },
            "feature_list": {
                "type": "array",
                "minItems": 1,
                "items": {
                    "type": "object",
                    "additionalProperties": False,
                    "required": ["id", "category", "description", "steps", "passes"],
                    "properties": {
                        "id": {"type": "string", "minLength": 2},
                        "category": {"type": "string", "minLength": 2},
                        "description": {"type": "string", "minLength": 5},
                        "steps": {
                            "type": "array",
                            "minItems": 1,
                            "items": {"type": "string", "minLength": 2},
                        },
                        "passes": {"type": "boolean"},
                    },
                },
            },
            "task_plan": {
                "type": "object",
                "additionalProperties": True,
                "required": ["project", "rules", "tasks"],
                "properties": {
                    "project": {"type": "string", "minLength": 1},
                    "rules": {
                        "type": "array",
                        "minItems": 1,
                        "items": {"type": "string", "minLength": 3},
                    },
                    "tasks": {
                        "type": "array",
                        "minItems": 1,
                        "items": {
                            "type": "object",
                            "additionalProperties": False,
                            "required": ["id", "title", "description", "steps", "passes"],
                            "properties": {
                                "id": {"type": "string", "minLength": 2},
                                "title": {"type": "string", "minLength": 3},
                                "description": {"type": "string", "minLength": 5},
                                "steps": {
                                    "type": "array",
                                    "minItems": 1,
                                    "items": {"type": "string", "minLength": 2},
                                },
                                "passes": {"type": "boolean"},
                            },
                        },
                    },
                },
            },
            "files": {
                "type": "array",
                "minItems": len(CORE_REQUIRED_PATHS),
                "items": {
                    "type": "object",
                    "additionalProperties": False,
                    "required": ["path", "content", "reason", "executable"],
                    "properties": {
                        "path": {"type": "string", "minLength": 5},
                        "content": {"type": "string"},
                        "reason": {"type": "string", "minLength": 3},
                        "executable": {"type": "boolean"},
                    },
                },
            },
        },
    }


def _build_codex_prompt(plan_path: Path, plan_text: str) -> str:
    required_paths_block = "\n".join(f"- {path}" for path in CORE_REQUIRED_PATHS)
    return f"""You are generating a complete Codex long-running research harness for a deep-learning project.

Goal:
Create a production-usable .codex-research artifact bundle from the provided plan.

Critical requirement:
Do NOT use simplistic keyword-only extraction logic. Infer concrete workflow details from the plan context,
including experiment environment, metrics, Slurm usage, reproducibility workflow, and validation strategy.

You must return JSON only matching the provided output schema.

Hard rules:
1. Generate full file content for every path in the mandatory core path list.
2. Add extra files under .codex-research/ when the plan implies they are needed (e.g., slurm configs, eval config, wandb config).
3. In feature_list and task_plan, every item must start with passes=false.
4. run_one_task.sh and run_plan.sh must drive one-task-per-session execution with codex exec.
5. smoke_test.sh must validate both feature_list.json and task_plan.json structure.
6. Use practical, detailed content: realistic metrics, environment assumptions, run commands, and blocker handling.
7. Do not generate files outside .codex-research/.
8. Return UTF-8 plain text content for each file (no base64).

Mandatory core paths:
{required_paths_block}

Source plan path: {plan_path.resolve()}
Source plan content starts below.
SOURCE_PLAN_START
{plan_text}
SOURCE_PLAN_END
"""


def _load_json(path: Path) -> Dict[str, Any]:
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        raise SystemExit(f"failed to parse JSON: {path}: {exc}") from exc


def _run_codex_bundle(plan_path: Path, target_root: Path, model: str) -> Dict[str, Any]:
    ensure_codex_cli()
    plan_text = read_text(plan_path)
    prompt = _build_codex_prompt(plan_path, plan_text)

    with tempfile.TemporaryDirectory() as tmp_dir:
        tmp = Path(tmp_dir)
        schema_path = tmp / "bundle_schema.json"
        output_path = tmp / "bundle_output.json"
        log_path = tmp / "codex_bundle.log"

        schema_path.write_text(as_json_pretty(_json_schema()), encoding="utf-8")

        cmd: List[str] = [
            "codex",
            "exec",
            "--skip-git-repo-check",
            "--ephemeral",
            "--sandbox",
            "read-only",
            "-C",
            str(target_root),
            "--output-schema",
            str(schema_path),
            "--output-last-message",
            str(output_path),
        ]
        if model:
            cmd.extend(["--model", model])

        proc = subprocess.run(
            cmd + ["-"],
            input=prompt,
            text=True,
            capture_output=True,
            check=False,
        )

        combined_log = f"STDOUT\n{proc.stdout}\n\nSTDERR\n{proc.stderr}\n"
        log_path.write_text(combined_log, encoding="utf-8")

        if proc.returncode != 0:
            raise SystemExit(
                "codex bundle generation failed "
                f"(exit={proc.returncode}). See temporary log: {log_path}"
            )

        if not output_path.exists():
            raise SystemExit("codex bundle generation produced no output JSON")

        return _load_json(output_path)


def _normalize_feature_list(raw: Any) -> List[Dict[str, Any]]:
    if not isinstance(raw, list):
        raise SystemExit("feature_list must be a JSON list")
    items: List[Dict[str, Any]] = []
    for idx, entry in enumerate(raw, start=1):
        if not isinstance(entry, dict):
            raise SystemExit(f"feature_list[{idx}] must be an object")
        item = {
            "id": str(entry.get("id", f"F-{idx:04d}")).strip() or f"F-{idx:04d}",
            "category": str(entry.get("category", "research")).strip() or "research",
            "description": str(entry.get("description", "")).strip(),
            "steps": entry.get("steps", []),
            "passes": False,
        }
        if not item["description"]:
            raise SystemExit(f"feature_list[{idx}].description is empty")
        if not isinstance(item["steps"], list) or not item["steps"]:
            raise SystemExit(f"feature_list[{idx}].steps must be a non-empty list")
        item["steps"] = [str(step).strip() for step in item["steps"] if str(step).strip()]
        if not item["steps"]:
            raise SystemExit(f"feature_list[{idx}].steps resolved to empty list")
        items.append(item)
    return items


def _normalize_task_plan(raw: Any, plan_path: Path, generated_at: str) -> Dict[str, Any]:
    if not isinstance(raw, dict):
        raise SystemExit("task_plan must be a JSON object")
    tasks = raw.get("tasks")
    if not isinstance(tasks, list) or not tasks:
        raise SystemExit("task_plan.tasks must be a non-empty list")

    normalized_tasks: List[Dict[str, Any]] = []
    for idx, entry in enumerate(tasks, start=1):
        if not isinstance(entry, dict):
            raise SystemExit(f"task_plan.tasks[{idx}] must be an object")
        task = {
            "id": str(entry.get("id", f"T-{idx:04d}")).strip() or f"T-{idx:04d}",
            "title": str(entry.get("title", "")).strip(),
            "description": str(entry.get("description", "")).strip(),
            "steps": entry.get("steps", []),
            "passes": False,
        }
        if not task["title"]:
            raise SystemExit(f"task_plan.tasks[{idx}].title is empty")
        if not task["description"]:
            raise SystemExit(f"task_plan.tasks[{idx}].description is empty")
        if not isinstance(task["steps"], list) or not task["steps"]:
            raise SystemExit(f"task_plan.tasks[{idx}].steps must be a non-empty list")
        task["steps"] = [str(step).strip() for step in task["steps"] if str(step).strip()]
        if not task["steps"]:
            raise SystemExit(f"task_plan.tasks[{idx}].steps resolved to empty list")
        normalized_tasks.append(task)

    rules = raw.get("rules")
    if not isinstance(rules, list) or not rules:
        rules = [
            "Exactly one task per Codex session.",
            "Do not set passes=true without test/command evidence.",
            "If blocked, keep passes=false and log blocker details.",
        ]

    return {
        "project": str(raw.get("project", plan_path.stem)).strip() or plan_path.stem,
        "generated_at": generated_at,
        "plan_path": str(plan_path.resolve()),
        "rules": [str(rule).strip() for rule in rules if str(rule).strip()],
        "tasks": normalized_tasks,
    }


def _ensure_text_with_newline(value: str) -> str:
    return value if value.endswith("\n") else value + "\n"


def _normalize_bundle(raw_bundle: Dict[str, Any], plan_path: Path) -> Dict[str, Any]:
    generated_at = iso_now()
    summary = str(raw_bundle.get("summary", "")).strip()
    if len(summary) < 20:
        raise SystemExit("codex summary is too short; expected detailed generation summary")

    feature_list = _normalize_feature_list(raw_bundle.get("feature_list"))
    task_plan = _normalize_task_plan(raw_bundle.get("task_plan"), plan_path, generated_at)

    required_map: Dict[str, str] = {}
    raw_required = raw_bundle.get("required_files")
    if not isinstance(raw_required, list) or not raw_required:
        raise SystemExit("required_files must be a non-empty list")
    for idx, entry in enumerate(raw_required, start=1):
        if not isinstance(entry, dict):
            raise SystemExit(f"required_files[{idx}] must be an object")
        path = normalize_rel_path(str(entry.get("path", "")))
        reason = str(entry.get("reason", "")).strip() or DEFAULT_REASON
        required_map[path] = reason

    file_map: Dict[str, str] = {}
    executable_paths: Set[str] = set()
    raw_files = raw_bundle.get("files")
    if not isinstance(raw_files, list) or not raw_files:
        raise SystemExit("files must be a non-empty list")

    for idx, entry in enumerate(raw_files, start=1):
        if not isinstance(entry, dict):
            raise SystemExit(f"files[{idx}] must be an object")
        path = normalize_rel_path(str(entry.get("path", "")))
        if path in file_map:
            raise SystemExit(f"duplicate generated file path: {path}")
        content = entry.get("content")
        if not isinstance(content, str):
            raise SystemExit(f"files[{idx}].content must be a string")
        file_map[path] = _ensure_text_with_newline(content)

        reason = str(entry.get("reason", "")).strip() or DEFAULT_REASON
        required_map[path] = reason

        executable = bool(entry.get("executable", False))
        if executable or path.endswith((".sh", ".sbatch")):
            executable_paths.add(path)

    missing_core = [path for path in CORE_REQUIRED_PATHS if path not in file_map]
    if missing_core:
        raise SystemExit(
            "codex-generated bundle is missing mandatory files: " + ", ".join(missing_core)
        )

    file_map[".codex-research/feature_list.json"] = as_json_pretty(feature_list)
    file_map[".codex-research/task_plan.json"] = as_json_pretty(task_plan)

    for path in CORE_REQUIRED_PATHS:
        required_map.setdefault(path, DEFAULT_REASON)

    for path in list(file_map):
        required_map.setdefault(path, DEFAULT_REASON)

    required_files = [
        {"path": path, "reason": required_map[path]}
        for path in sorted(required_map.keys())
    ]

    required_payload = {
        "generated_at": generated_at,
        "plan_path": str(plan_path.resolve()),
        "source": "codex",
        "summary": summary,
        "required_files": required_files,
    }
    file_map[".codex-research/required_files.json"] = as_json_pretty(required_payload)

    return {
        "generated_at": generated_at,
        "plan_path": str(plan_path.resolve()),
        "source": "codex",
        "summary": summary,
        "required_files": required_files,
        "feature_list": feature_list,
        "task_plan": task_plan,
        "file_map": file_map,
        "executable_paths": sorted(executable_paths),
    }


def load_bundle(plan_path: Path, target_root: Path, codex_model: str) -> Dict[str, Any]:
    raw = _run_codex_bundle(plan_path=plan_path, target_root=target_root, model=codex_model)
    return _normalize_bundle(raw, plan_path=plan_path)


def write_file(path: Path, content: str, force: bool) -> Tuple[bool, str]:
    if path.exists() and not force:
        return False, "exists"
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    return True, "written"


def chmod_if_shell(path: Path) -> None:
    if path.suffix in {".sh", ".sbatch"}:
        current = path.stat().st_mode
        path.chmod(current | 0o111)


def cmd_extract(args: argparse.Namespace) -> int:
    plan_path = Path(args.plan).resolve()
    target_root = Path(args.target).resolve() if args.target else plan_path.parent
    out_path = Path(args.out).resolve()

    bundle = load_bundle(plan_path=plan_path, target_root=target_root, codex_model=args.codex_model)

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(
        as_json_pretty(
            {
                "generated_at": bundle["generated_at"],
                "plan_path": bundle["plan_path"],
                "source": bundle["source"],
                "summary": bundle["summary"],
                "required_files": bundle["required_files"],
                "feature_list": bundle["feature_list"],
                "task_plan": bundle["task_plan"],
            }
        ),
        encoding="utf-8",
    )
    print(f"[extract] wrote {out_path}")
    return 0


def bootstrap_from_bundle(
    target_root: Path,
    bundle: Dict[str, Any],
    force: bool,
) -> Tuple[int, int]:
    file_map: Dict[str, str] = bundle["file_map"]
    executable_paths: Set[str] = set(bundle["executable_paths"])

    created = 0
    skipped = 0

    for rel_path in sorted(file_map.keys()):
        abs_path = target_root / rel_path
        content = file_map[rel_path]
        wrote, status = write_file(abs_path, content, force)
        if wrote:
            if rel_path in executable_paths or abs_path.suffix in {".sh", ".sbatch"}:
                chmod_if_shell(abs_path)
            created += 1
            print(f"[bootstrap] created {abs_path}")
        else:
            skipped += 1
            print(f"[bootstrap] skipped {abs_path} ({status})")

    return created, skipped


def cmd_bootstrap(args: argparse.Namespace) -> int:
    plan_path = Path(args.plan).resolve()
    target_root = Path(args.target).resolve()
    bundle = load_bundle(plan_path=plan_path, target_root=target_root, codex_model=args.codex_model)

    created, skipped = bootstrap_from_bundle(target_root, bundle, args.force)
    print(f"[bootstrap] done: created={created}, skipped={skipped}")
    return 0


def cmd_gen_doc(args: argparse.Namespace) -> int:
    plan_path = Path(args.plan).resolve()
    target_root = Path(args.target).resolve()
    bundle = load_bundle(plan_path=plan_path, target_root=target_root, codex_model=args.codex_model)

    mechanism_path = target_root / ".codex-research/MECHANISM.md"
    mechanism_content = bundle["file_map"].get(".codex-research/MECHANISM.md")
    if not mechanism_content:
        raise SystemExit("codex bundle missing .codex-research/MECHANISM.md")

    mechanism_path.parent.mkdir(parents=True, exist_ok=True)
    mechanism_path.write_text(mechanism_content, encoding="utf-8")
    print(f"[doc] wrote {mechanism_path}")
    return 0


def cmd_all(args: argparse.Namespace) -> int:
    plan_path = Path(args.plan).resolve()
    target_root = Path(args.target).resolve()
    bundle = load_bundle(plan_path=plan_path, target_root=target_root, codex_model=args.codex_model)

    extract_path = target_root / ".codex-research/required_files.generated.json"
    extract_path.parent.mkdir(parents=True, exist_ok=True)
    extract_path.write_text(
        as_json_pretty(
            {
                "generated_at": bundle["generated_at"],
                "plan_path": bundle["plan_path"],
                "source": bundle["source"],
                "summary": bundle["summary"],
                "required_files": bundle["required_files"],
                "feature_list": bundle["feature_list"],
                "task_plan": bundle["task_plan"],
            }
        ),
        encoding="utf-8",
    )

    created, skipped = bootstrap_from_bundle(target_root, bundle, args.force)

    mechanism_path = target_root / ".codex-research/MECHANISM.md"
    print(f"[all] wrote manifest: {extract_path}")
    print(f"[all] wrote mechanism: {mechanism_path}")
    print(f"[all] done: created={created}, skipped={skipped}")
    return 0


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Codex-driven long-running harness bootstrapper for DL research.",
    )
    sub = parser.add_subparsers(dest="cmd", required=True)

    extract = sub.add_parser("extract", help="Generate Codex-derived manifest from a plan")
    extract.add_argument("--plan", required=True, help="Path to uploaded plan file")
    extract.add_argument("--out", required=True, help="Output JSON path")
    extract.add_argument(
        "--target",
        required=False,
        help="Optional target project root for Codex execution context",
    )
    extract.add_argument("--codex-model", default="", help="Optional Codex model override")
    extract.set_defaults(func=cmd_extract)

    bootstrap = sub.add_parser("bootstrap", help="Create harness files in target project")
    bootstrap.add_argument("--plan", required=True, help="Path to uploaded plan file")
    bootstrap.add_argument("--target", required=True, help="Target project root")
    bootstrap.add_argument("--force", action="store_true", help="Overwrite existing files")
    bootstrap.add_argument("--codex-model", default="", help="Optional Codex model override")
    bootstrap.set_defaults(func=cmd_bootstrap)

    gen_doc = sub.add_parser("gen-doc", help="Generate mechanism markdown in project")
    gen_doc.add_argument("--plan", required=True, help="Path to uploaded plan file")
    gen_doc.add_argument("--target", required=True, help="Target project root")
    gen_doc.add_argument("--codex-model", default="", help="Optional Codex model override")
    gen_doc.set_defaults(func=cmd_gen_doc)

    all_cmd = sub.add_parser("all", help="Extract + bootstrap + generate doc")
    all_cmd.add_argument("--plan", required=True, help="Path to uploaded plan file")
    all_cmd.add_argument("--target", required=True, help="Target project root")
    all_cmd.add_argument("--force", action="store_true", help="Overwrite existing files")
    all_cmd.add_argument("--codex-model", default="", help="Optional Codex model override")
    all_cmd.set_defaults(func=cmd_all)

    return parser


def main() -> int:
    parser = build_parser()
    args = parser.parse_args()

    plan_path = Path(getattr(args, "plan", ""))
    if plan_path and not plan_path.exists():
        raise SystemExit(f"Plan file not found: {plan_path}")

    return args.func(args)


if __name__ == "__main__":
    raise SystemExit(main())
